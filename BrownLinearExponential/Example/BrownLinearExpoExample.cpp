// Brown Linear Exponential filter

#include "BrownLinearExpo.h"
#include <stdio.h>
#include "stdlib.h"
#include <iostream>
//#include <windows.h>

using namespace std;

int main(int argc, char* argv[])
{

double xm;
double e1,e2,e3;
BrownLinearExpo BLE1(.1);
BrownLinearExpo BLE4(.4);
BrownLinearExpo BLE7(.7);

for (int i=0; i <= 200; i++)
	{
		xm = 0.;// input signal
		if (i == 0) { BLE1.reset(1.); BLE4.reset(1.); BLE7.reset(1.); } // initialize Brown
		e1 = BLE1.step(xm);
		e2 = BLE4.step(xm);
		e3 = BLE7.step(xm);

		fprintf(stdout, "%d, %f, %f, %f, %f\n",i, xm, e1, e2, e3);
	}
	
	std::cout << std::endl;

	return 0;
}
